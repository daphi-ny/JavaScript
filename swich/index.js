var mes = 'agosto';
switch (mes) {
    case 'janeiro':
    case 'fevereiro':
    case 'março':
        document.write('primavera');
        break;

    case 'abril':
    case 'maio':
    case 'junho':
        document.write('outono');
        break;   
        
    case 'julho':
    case 'agosto':
    case 'setembro':
        document.write('inverno');
        break;      

    case 'outrubro':
    case 'novembro':
    case 'dezembro':
        document.write('verão');
        break;

}
